let rightGuessString = TEACHERS[Math.floor(Math.random() * TEACHERS.length)] 
let wordLength = rightGuessString.length
const words = wordLength/2 +3;
let guessesRemaining = NUMBER_OF_GUESSES;
let currentGuess = [];
let nextLetter = 0;

